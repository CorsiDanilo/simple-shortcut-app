/**
 * Renders a named Material icon into a Bitmap suitable for ShortcutInfoCompat.
 * Size: 192x192 px (adaptive icon inner area).
 */
fun materialIconBitmap(iconName: String, context: Context): Bitmap {
    val size = 192
    val bmp = Bitmap.createBitmap(size, size, Bitmap.Config.ARGB_8888)
    val canvas = Canvas(bmp)

    // Draw circular background
    val bgPaint = Paint(Paint.ANTI_ALIAS_FLAG).apply {
        color = 0xFF6650A4.toInt() // primary color
    }
    canvas.drawCircle(size / 2f, size / 2f, size / 2f, bgPaint)

    val pathString = ICON_PATHS[iconName]
    if (pathString != null) {
        val path = PathParser.createPathFromPathData(pathString)
        
        // Scale the path from 24x24 to 120x120, and center it in 192x192
        val iconSize = 120f
        val scale = iconSize / 24f
        val offset = (size - iconSize) / 2f
        
        val matrix = Matrix()
        matrix.postScale(scale, scale)
        matrix.postTranslate(offset, offset)
        path.transform(matrix)

        val iconPaint = Paint(Paint.ANTI_ALIAS_FLAG).apply {
            color = 0xFFFFFFFF.toInt()
            style = Paint.Style.FILL
        }
        canvas.drawPath(path, iconPaint)
    } else {
        // Fallback to text letter if path is missing
        val textPaint = Paint(Paint.ANTI_ALIAS_FLAG).apply {
            color = 0xFFFFFFFF.toInt()
            textSize = 96f
            textAlign = Paint.Align.CENTER
        }
        val label = iconName.firstOrNull()?.toString() ?: "?"
        val yPos = size / 2f - (textPaint.descent() + textPaint.ascent()) / 2f
        canvas.drawText(label, size / 2f, yPos, textPaint)
    }

    return bmp
}
